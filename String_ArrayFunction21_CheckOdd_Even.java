package automationBatch62;

public class String_ArrayFunction21_CheckOdd_Even 
{
	public static void main(String[] args) 
	{
		int[] NumbersSet=new int[5];
		NumbersSet[0]=13;
		NumbersSet[1]=14;
		NumbersSet[2]=17;
		NumbersSet[3]=18;
		NumbersSet[4]=27;
		
		for(int i=0;i<NumbersSet.length;i++)
		{
			if (NumbersSet[i]%2==0)
				{
				System.out.println("Given No is even>:"+  NumbersSet[i]+ "At index"+  i  );
				}
			if(NumbersSet[i]%2==1)
			{
				System.out.println("Given No is odd>:"+   NumbersSet[i]+ "At index"+  i  );
			}
			
				
		}
		  
		 
		
	}
	
		
	}
	
	
	
		
		
		
		
			

	

	
	
		
		
		
		
		

	

	
	



